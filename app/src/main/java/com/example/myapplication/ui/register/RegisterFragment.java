    public void register(View view) {
        String username = binding.username.getText().toString();
        String password = binding.password.getText().toString();
        String passwordConfirm = binding.confirmPassword.getText().toString();

        if (!password.equals(passwordConfirm)) {
            return;
        }

        mAuth.createUserWithEmailAndPassword(username, password).addOnCompleteListener(getActivity(), new OnCompleteListener<AuthResult>() {
            @Override
            public void onComplete(@NonNull Task<AuthResult> task) {
                if (task.isSuccessful()) {
                    Toast.makeText(getActivity(), R.string.register_success, Toast.LENGTH_LONG).show();
                    Navigation.findNavController(view).navigate(R.id.nav_newswire);
                }
                else {
                    Toast.makeText(getActivity(), R.string.register_failed, Toast.LENGTH_LONG).show();
                }
            }
        });
    }

